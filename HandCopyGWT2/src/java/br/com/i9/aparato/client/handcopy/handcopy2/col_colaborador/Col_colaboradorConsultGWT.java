    private GridCellRenderer<Col_colaboradorTGWT> getEditarRender() {
        return new GridCellRenderer<Col_colaboradorTGWT>() {

            private boolean init;

            public Object render(final Col_colaboradorTGWT model, String property, ColumnData config, final int rowIndex,
                    final int colIndex, ListStore<Col_colaboradorTGWT> store, Grid<Col_colaboradorTGWT> grid) {
                if (!init) {
                    init = true;
                    grid.addListener(Events.ColumnResize, new Listener<GridEvent<Col_colaboradorTGWT>>() {

                        public void handleEvent(GridEvent<Col_colaboradorTGWT> be) {
                            for (int i = 0; i < be.getGrid().getStore().getCount(); i++) {
                                if (be.getGrid().getView().getWidget(i, be.getColIndex()) != null
                                        && be.getGrid().getView().getWidget(i, be.getColIndex()) instanceof BoxComponent) {
                                    ((BoxComponent) be.getGrid().getView().getWidget(i, be.getColIndex())).setWidth(be.getWidth() - 10);
                                }
                            }
                        }
                    });
                }

                Button b = new Button();
                b.addListener(Events.OnClick, new Listener<ButtonEvent>() {

                    public void handleEvent(ButtonEvent be) {
                        Col_colaboradorUpdateDeleteGWT col_colaboradorUpdateDeleteGWT = new Col_colaboradorUpdateDeleteGWT();
                        col_colaboradorUpdateDeleteGWT.setCol_colaboradorConsult(Col_colaboradorConsultGWT.this);
                        col_colaboradorUpdateDeleteGWT.load(model);
                        col_colaboradorUpdateDeleteGWT.show();
                    }
                });

                b.setWidth(grid.getColumnModel().getColumnWidth(colIndex) - 10);
                b.setToolTip("Alterar dados.");
                b.setIcon(ICONS.edit());

                return b;
            }
        };
    }

  public void load() {
    col_colaboradorDao.consultarTodos();
    Timer timer = new Timer() {
            public void run() {
                ListStore<Col_colaboradorTGWT> list = col_colaboradorDao.getList();
                if (list == null) {
                    schedule(500);
                } else {
                    List lista = getCpMaster().getItems();
                    if (lista.size() > 0) {
                        getCpMaster().removeAll();
                    }

                    ColumnModel cm = new ColumnModel(configs);

                    Grid<Col_colaboradorTGWT> grid = new Grid<Col_colaboradorTGWT>(list, cm);
                    grid.setLoadMask(true);

                    grid.setStyleAttribute("borderTop", "none");
                    grid.setBorders(true);
                    grid.getSelectionModel().setSelectionMode(SelectionMode.SINGLE);

                    getCpMaster().add(grid);
                    getCpMaster().layout();
                }
            }
    };
    timer.schedule(500);
  }
